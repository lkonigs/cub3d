#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n <= 0)
		return (0);
	while (*s1 && *s2 && *s1 == *s2 && (i < n))
	{
		i++;
		s1++;
		s2++;
	}
	if (i < n)
		return (*(unsigned char *)s1 - *(unsigned char *)s2);
	return (0);
}

int	ft_strcmp(const char *s1, const char *s2)
{
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return (*(unsigned char *)s1 - *(unsigned char *)s2);
}